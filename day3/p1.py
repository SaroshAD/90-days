print(x//y)    #floor division for quotient



# logical operators

print("x is less than y:", x < y)       #less than
print("x is greater than y:", x > y)        #greater than
print("x is equal to y:", x == y)      #equal to
print("x is not equal to y:", x != y)        #not equal to
